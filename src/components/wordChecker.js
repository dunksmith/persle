
var checkWord = function (word) {

  var match = getName();

  if (word === "CHEAT") alert(match);
  if (word === "FLYNN") alert("That's my name don't wear it out!");

  if (names.every(x => x.name !== word)) return null;

  var result = [];

  for(var i=0; i<word.length; i++) {
    if (word[i] === match[i])
      // Match!
      result.push(2);
    else if (match.split("").every(x => x !== word[i])) {
      // Nope :(
      result.push(0);
    }
    else {
      // Could be elsewhere in word - handle multiple
      var upToThisPoint = word.substring(0, i).split("").filter(x => x === word[i]).length;

      // Exclude exact matches
      var total = match.split("").filter((x,j) => x === word[i] && match[j] !== word[j]).length;

      if (upToThisPoint >= total)
        result.push(0);
      else
        result.push(1)
    }
  }

  // Second pass - if matched elsewhere

  return result;
};

var getName = function() {
    // Days from day 0
    var ms = new Date() - new Date("2022-04-08T00:00");
    var day = Math.floor(ms / 1000 / 60 / 60 / 24);
  
    return names[day].name;  
};

